            <ol>
              <li>Changes in genotype can result in changes in phenotype.</li>
              <li>Biological systems have multiple processes that increase genetic variation.</li>
              <li>Viral replication results in genetic variation and viral infection can introduce genetic variation into the hosts.</li>
            </ol>
          </div>
        </div>
