              <li>Cells communicate with each other through direct contact with other cells or from a distance via chemical signaling.</li>
              <li>Signal transduction pathways link signal reception with cellular response.</li>
              <li>Changes in signal transduction pathways can alter cellular response.</li>
            </ol>
